# 문장 (statement)
# 실행 할 수 있는 코드의 최소 단위
# 엔터쳐서 정상적으로 실행되면 문장 10 + 10 쳐서 실행 되니까 문장
# 엔터 쳤는데 다음 줄로 넘어가면 비어있는 문장 null 문장
# if true: 같이 한 줄로 실행 안되는 문장 복합문장

#표현식(expression) 어떤 값을 만들어내는 코드 10은 문장이자 표현식
#10 + 10은 20 도 표현식 

# 리터럴(literal) 리터럴 < 표현식 < 문장  10 20 30 같은 단일 값
# 리터럴로 값을 만들어서 표현식이 되고 그 표현식이 실행이 되니까 문장이 된다.

#연산자(operator) 단독으로는 문장이 될 수 없다 
